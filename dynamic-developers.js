        // Function to load developer data
        function loadDeveloperData(developerKey) {
            const developer = developers[developerKey];
            if (!developer) {
                // Redirect to default if developer not found
                window.location.href = 'dynamic-developers.html?developer=emaar';
                return;
            }
            
            // Update page title
            document.title = `${developer.name} | Premier Real Estate Developer`;
            
            // Update hero section class and content
            const heroSection = document.querySelector('.developers-hero');
            heroSection.className = developer.heroClass;
            document.getElementById('developer-name').textContent = developer.name;
            document.getElementById('developer-tagline').textContent = developer.tagline;
            
            // Update developer details
            document.getElementById('developer-logo').src = developer.logo;
            document.getElementById('developer-logo').alt = `${developer.name} Logo`;
            document.getElementById('developer-title').textContent = developer.title;
            
            // Update descriptions
            document.getElementById('developer-desc-1').textContent = developer.descriptions[0];
            document.getElementById('developer-desc-2').textContent = developer.descriptions[1];
            document.getElementById('developer-desc-3').textContent = developer.descriptions[2];
            
            // Update projects
            const projectsContainer = document.getElementById('projects-container');
            projectsContainer.innerHTML = '';
            
            developer.projects.forEach((project, index) => {
                const animationClass = index % 3 === 0 ? 'slide-in-left delay-4' : 
                                      index % 3 === 1 ? 'slide-in-up delay-2' : 
                                      'slide-in-right delay-4';
                
                const projectHTML = `
                    <div class="developers-card animate-on-scroll ${animationClass}">
                        <div class="developers-card-img">
                            <img src="${project.image}" alt="${project.name}">
                        </div>
                        <div class="developers-card-content">
                            <h3>${project.name}</h3>
                            <p>${project.description}</p>
                        </div>
                    </div>
                `;
                
                projectsContainer.innerHTML += projectHTML;
            });
            
            // Update active state in navigation
            updateNavbarActiveState(developerKey);
        }

        // Get developer from URL parameter
        function getDeveloperFromURL() {
            const urlParams = new URLSearchParams(window.location.search);
            return urlParams.get('developer') || 'emaar'; // Default to emaar
        }

        // Update navbar active state
        function updateNavbarActiveState(developerKey) {
            // Remove active class from all developer links
            document.querySelectorAll('.dropdown-item').forEach(item => {
                item.classList.remove('active');
            });
            
            // Add active class to current developer
            const currentDeveloperLink = document.querySelector(`[href*="${developerKey}"]`);
            if (currentDeveloperLink) {
                currentDeveloperLink.classList.add('active');
            }
        }

        // Initialize page
        document.addEventListener('DOMContentLoaded', function() {
            const developerKey = getDeveloperFromURL();
            loadDeveloperData(developerKey);
        });
    